/* 
Operadores de comparação

> - Maior que
>= - Maior ou igual a
< - Menor que
<= - Menor ou igual a
== - igual a
=== - Igual a (checa o valor e tipo)
!= - Difente a
!== - Diferente a (checa o valor e tipo)

*/
console.log(5 > 2); // true
console.log(5 >= 5); // true
console.log(5 < 2); // false
console.log(5 <= 5); // true
console.log(5 == '5'); // true
console.log(5 === '5'); // false
console.log(5 != '5'); // false
console.log(5 !== '5'); // true
